#import "UIFont+Feels.h"

@implementation UIFont (Feels)

+ (UIFont *)GeoSansLight:(float)size {
    return [UIFont fontWithName:@"GeosansLight" size:size];
}

+ (UIFont *)AvantGardeExtraLight:(float)size {
    return [UIFont fontWithName:@"ITCAvantGardeStd-XLt" size:size];
}

+ (UIFont *)GeogrotesqueGardeExtraLight:(float)size {
    return [UIFont fontWithName:@"Geogrotesque" size:size];
}

@end
